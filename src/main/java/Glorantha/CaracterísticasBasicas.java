package Glorantha;

public class CaracterísticasBasicas {

	private int habilidadOfensiva;
	private int habilidadDefensiva;
	private int velocidadAtaque;
	public int getHabilidadOfensiva() {
		return habilidadOfensiva;
	}
	public void setHabilidadOfensiva(int habilidadOfensiva) {
		this.habilidadOfensiva = habilidadOfensiva;
	}
	public int getHabilidadDefensiva() {
		return habilidadDefensiva;
	}
	public void setHabilidadDefensiva(int habilidadDefensiva) {
		this.habilidadDefensiva = habilidadDefensiva;
	}
	public int getVelocidadAtaque() {
		return velocidadAtaque;
	}
	public void setVelocidadAtaque(int velocidadAtaque) {
		this.velocidadAtaque = velocidadAtaque;
	}
	
	
}
